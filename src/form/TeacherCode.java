     private boolean isTeacherRegistered(String tname, String ccode, String password) {
    try {
        String sql = "SELECT password FROM teachers WHERE teacher_name = ? AND course_code = ?";
        PreparedStatement pstmt = con.prepareStatement(sql);
        pstmt.setString(1, tname);
        pstmt.setString(2, ccode);
        ResultSet rs = pstmt.executeQuery();

        if (rs.next()) {
            String storedHashedPassword = rs.getString("password");

            // Hash the entered password and compare it with the stored hash
            if (storedHashedPassword.equals(hashPassword(password))) {
                return true;
            }
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Database Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    return false;
}
        private String hashPassword(String password) {
    try {
        MessageDigest md = MessageDigest.getInstance("SHA-256");
        byte[] hashedBytes = md.digest(password.getBytes());
        StringBuilder hexString = new StringBuilder();
        for (byte b : hashedBytes) {
            hexString.append(String.format("%02x", b));
        }
        return hexString.toString();
    } catch (NoSuchAlgorithmException e) {
        JOptionPane.showMessageDialog(this, "Error hashing password!", "Error", JOptionPane.ERROR_MESSAGE);
        return null;
    }
}

